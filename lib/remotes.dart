import 'package:flutter/material.dart';

import 'main.dart';
class RemoteScreen1 extends StatelessWidget {
  @override
  Widget build(BuildContext context) {

    Widget _buildRemoteWidget() => Container(
          decoration: BoxDecoration(
            gradient: LinearGradient(
              colors: [
                Color(0xFF3d3d44),
                Color(0xFF8e8e96),
                Color(0xFF3d3d44),
              ],
              begin: Alignment.topCenter,
              end: Alignment.bottomCenter,
            ),
          ),
          child: Column(
            children: [
              SizedBox(height: AppBar().preferredSize.height),
              RowOfButtons([0, 1, 2]),
              SizedBox(height: 10),
              RowOfButtons([3, 4, 5]),
              SizedBox(height: 10),
              RowOfButtons([6, 7, 8]),
            ],
          ),
        );

    return Scaffold(
      appBar: AppBar(
        centerTitle: true,
        title: Text('Remote 1'),
      ),
      body: _buildRemoteWidget(),
    );
  }
}



